    private void selected_instrumentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selected_instrumentActionPerformed
        // TODO add your handling code here:
        InstrumentosBase elemento = (InstrumentosBase) selected_instrument.getSelectedItem();
        // Detiene la música del instrumento anterior
        if (instrumento_presentando != null) {
            instrumento_presentando.DetenerSonido();
        }
        //El título del texto
        txt_title.setText(elemento.getNombre());
        //El descripción 
        txtarea_description.setText(elemento.DescripcionInstrumento());
        txtarea_description.setLineWrap(true);
        txtarea_description.setBorder(BorderFactory.createEmptyBorder(10, 10, 10, 10));

        //Imagen
        int ancho = lbl_imageinstrument.getWidth();
        int alto = lbl_imageinstrument.getHeight();
        //Cargar imagen en el label
        lbl_imageinstrument.setIcon(elemento.CargarImagen(ancho, alto));
        // Actualizar el instrumento actual
        instrumento_presentando = elemento;
    }//GEN-LAST:event_selected_instrumentActionPerformed

