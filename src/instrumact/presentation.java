    public presentation() {
        initComponents();
        // Crear el modelo para el JList
        InstrumentosBase guitarra = new Guitarra();
        InstrumentosBase acordeon = new Acordeon();
        InstrumentosBase tambor = new Tambor();
        InstrumentosBase piano = new Piano();
        InstrumentosBase saxofon = new Saxofon();
        InstrumentosBase violin = new Violin();
        //Se crea el array list
        ArrayList<InstrumentosBase> lista = new ArrayList<>();
        //Se agregan los elementos al array list
        lista.add(guitarra);
        lista.add(acordeon);
        lista.add(tambor);
        lista.add(piano);
        lista.add(saxofon);
        lista.add(violin);
        //Se crea un foreach con la lista para recorrer todo el array
        for (InstrumentosBase instrumento : lista) {
            selected_instrument.addItem(instrumento);
            listModel.addElement(instrumento);
        }
        list_name.setModel(listModel);
    }

